#!/usr/bin/env python3
"""
Takes two string of type string as arguments.
:string str1: First string
:string str2: Second string
:return: The sum of a and b of type string
"""


def concat(str1: str, str2: str) -> str:
    """
    Takes two string of type string as arguments.
    :string str1: First string
    :string str2: Second string
    :return: The sum of a and b of type string
    """
    return str1 + str2
