#!/usr/bin/env python3
"""
function sum_list take list as argument.
:input_list List: float
:return: sum list
"""
from typing import List


def sum_list(input_list: List[float]) -> float:
    """
    function sum_list take list as argument.
    :input_list List: float
    :return: sum list
    """
    return sum(input_list)
