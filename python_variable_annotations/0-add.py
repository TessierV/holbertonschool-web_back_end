#!/usr/bin/env python3
"""
Takes two numbers of type float as arguments.
:param a: First number of type float
:param b: Second number of type float
:return: The sum of a and b of type float
"""


def add(a: float, b: float) -> float:
    """
    Takes two numbers of type float as arguments.
    :param a: First number of type float
    :param b: Second number of type float
    :return: The sum of a and b of type float
    """
    return a + b
